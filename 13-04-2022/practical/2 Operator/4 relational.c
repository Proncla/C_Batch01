#include<stdio.h>


///relational   (==, !=, >, <, >=,<=)


int main()
{
    int age = 15,Age = 42;


    if(Age == age )
    {
        printf("1true");
    }

    if(Age != age)
    {
        printf("2true\n");

    }

    if(Age > age)
    {
        printf("3true\n");

    }

    if(Age<age)
    {
        printf("4true\n");
    }

    if(Age<=age)
    {
        printf("5true\n");
    }

    if(Age>=age)
    {
        printf("6true\n");
    }

}
